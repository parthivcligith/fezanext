export function getFinalPrice(size: string, thickness: number, slug: string = "pure-rest-mattress", top?: string | null) {
    const [length, width] = size.split(" x ").map(Number);
    const list = PRICE_LISTS[slug] || PRICE_LISTS["pure-rest-mattress"];
    
    const item = list.find(p =>
        p.size.length === length &&
        p.size.width === width &&
        p.size.thickness === thickness
    );

    if (!item) return 0;

    const discount = 0.35;
    let finalPrice = Math.round(item.price * (1 - discount));

    // Increase amount by 800 for Euro Top and 2000 for Pillow Top
    if (top === "Euro Top") {
        finalPrice += 800;
    } else if (top === "Pillow Top") {
        finalPrice += 2000;
    }

    return finalPrice;
}
